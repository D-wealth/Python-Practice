# SETS
set_1 = {'Apple', 'Banana', 'Cherry'}
set_2 = {'Pineapple', 'Pawpaw', 'Papaya', 'Apple'}
print(set_1)
print(set_2)

# SET INTERSECTION
intersection = set_1.intersection(set_2)
print(intersection)

# SET DIFFERENCE
diff = set_1.difference(set_2)
print(diff)

# SET UNION
union = set_1.union(set_2)
print(union)
