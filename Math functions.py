# MATH FUNCTIONS IN PYTHON
import math
# MATH.CEIL() 
math_ceil = math.ceil(23.6)
print(math_ceil)

# MATH.FLOOR()
math_floor = math.floor(12.4)
print(math_floor)

# MATH.FABS() : This will automatically returns a positive result of a negative fabs    
math_fabs = math.fabs(-45.9)
print(math_fabs)

